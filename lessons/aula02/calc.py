b = 2
c = 5

print(a, '+', b, '=', a + b)    # sum 
print(a, '-', c, '=', a - c)    # subtraction
print(b, 'x', c, '=', b * c)    # multiplication
print(a, '/', b, '=', a / b)    # division
print(c, '^', b, '=', c ** b)   # exponetial